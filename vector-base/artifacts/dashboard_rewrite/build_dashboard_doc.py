def set_font(run, *, size: int | None = None, bold: bool | None = None, color: str | None = None) -> None:
    run.font.name = "Arial"
    if size is not None:
        run.font.size = Pt(size)
    if bold is not None:
        run.bold = bold
    if color is not None:
        run.font.color.rgb = RGBColor.from_string(color)


def add_para(doc: Document, text: str = "", *, style: str | None = None) -> None:
    p = doc.add_paragraph(style=style)
    p.paragraph_format.space_after = Pt(8)
    p.paragraph_format.line_spacing = 1.15
    run = p.add_run(text)
    set_font(run, size=11)


def add_heading(doc: Document, text: str, level: int) -> None:
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(20 if level == 1 else 16 if level == 2 else 12)
    p.paragraph_format.space_after = Pt(6 if level < 3 else 4)
    run = p.add_run(text)
    size = 20 if level == 1 else 16 if level == 2 else 14
    color = "000000" if level < 3 else "434343"
    set_font(run, size=size, color=color)


def add_bullets(doc: Document, items: list[str]) -> None:
    for item in items:
        p = doc.add_paragraph(style="List Bullet")
        p.paragraph_format.space_after = Pt(4)
        p.paragraph_format.line_spacing = 1.15
        run = p.add_run(item)
        set_font(run, size=11)


def add_block(doc: Document, title: str, path: str, search: str, paragraphs: list[str], bullets: list[str] | None = None) -> None:
    add_heading(doc, title, 2)
    add_para(doc, f"Путь в интерфейсе: {path}")
    add_para(doc, f"Поисковые формулировки: {search}")
    for paragraph in paragraphs:
        add_para(doc, paragraph)
    if bullets:
        add_bullets(doc, bullets)


